export const getTodayQuote = () => {
  const now = new Date();
  const dayOfYear = Math.floor((now - new Date(now.getFullYear(), 0, 0)) / 86400000);
  const year = now.getFullYear();
  const offset = (year * 7) % quotes.length;
  const index = (dayOfYear + offset) % quotes.length;
  return quotes[index];
};
